    fun getAartisByCategory(category: AartiCategory): List<Aarti> {
        return if (category == AartiCategory.ALL) {
            getAllAartis()
        } else {
            getAllAartis().filter { it.category == category }
        }
    }

    fun searchAartis(query: String): List<Aarti> {
        return getAllAartis().filter {
            it.title.contains(query, ignoreCase = true) ||
                    it.deity.contains(query, ignoreCase = true)
        }
    }
